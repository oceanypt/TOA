V neděli Jihoafrická republika oficiálně uctila památku FW de Klerka, posledního prezidenta bílé nadvlády, který propustil Nelsona Mandelu z vězení a přivedl zemi z apartheidu k demokracii.
De Klerk zemřel 11. listopadu ve věku 85 let po boji s rakovinou.
Bylo vyhlášeno čtyřdenní národní smutné období na jeho počest.
V letech 1989 až 1994 zastával funkci prezidenta a je nejvíce vzpomínán za to, že vedl přechod od bělošské menšinové vlády k prvním víceetnickým volbám v roce 1994.
De Klerk byl v roce 1993 spolu s Mandelou oceněn Nobelovou cenou míru za to, že jej v roce 1990 osvobodil z vězení.
Mandela se pak stal prvním černošským prezidentem Jihoafrické republiky po vítězství jeho strany Africký národní kongres v roce 1994.
Prezident Cyril Ramaphosa v neděli ráno navštívil protestantský kostel Groote Kerk v Kapském Městě, jeden z nejstarších kostelů v Jihoafrické republice, aby zde vzdal hold De Klerkovi.
„Byl často nesprávně chápán kvůli své přecitlivělosti,“ řekla Elita Georgiadisová, vdova po De Klerkovi, zhruba 200 přítomným.
Nikdy na něj nezapomenu, byl to člověk, který mě okouzlil a který mě přesvědčil, že chci mu pomoci v jeho obrovském úkolu.
Soukromá mše a zpívání národní hymny předcházely slavnostnímu ceremoniálu, během něhož byl portrét De Klerka umístěn mezi dvě svíčky a sbor byl ozdoben bílými květinami.
Ačkoliv měl v zahraničí pozitivní pověst, De Klerk byl v Jihoafrické republice kontroverzní postavou a jeho smrt vyvolala smíšené reakce.
Kritici tvrdí, že zůstává nerozlučně spjatý s trestnými činy z éry apartheidu a mohl být za ně postižen, kdyby žil déle.
De Klerk zastupoval Národní stranu, která v roce 1948 formálně zavedla rasovou segregaci a vyloučila z volebního práva většinu nebílých obyvatel Jihoafrické republiky.
Před kostelem se shromáždila malá skupina protestujících, kteří drželi transparenty s nápisy „Spravedlnost odmítnuta“ a „Spravedlnost pro oběti apartheidu“ a policie je rychle odvedla pryč.
