Test molekulární diagnostiky dokáže za 20 minut detekovat variantu omikron: Zpráva
Korejští vědci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicron.
Vývoj technologie je nyní dokončený a očekává se, že komercializace bude trvat delší dobu.
POSTECH oznámil 10. prosince, že výzkumný tým pod vedením profesora Lee Jung-wooka z Katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicron během 20–30 minut a výsledky zveřejní online.
Varianta omikron obsahuje 26–32 mutací v proteinu spike, který se využívá k infikování buněk virem COVID-19.
Podle výzkumného týmu je molekulární diagnostická technologie schopná detekovat mutace na úrovni jednotlivých nukleotidových bází, a proto dokáže odhalit „Stealth Omicron“, který je obtížné detekovat pomocí PCR testů.
V současné době Korejské centrum pro kontrolu a prevenci nemocí používá k detekci variant COVID-19 tři metody: analýzu celého genomu, analýzu cílové DNA (mutace jako například spike protein) a PCR test.
U varianty Delta je možné nákazu detekovat pomocí současného PCR testu, u varianty Omicron to však nelze.
Nově vyvinutá technologie tentokrát není metoda sekvenování DNA nebo RNA, ale molekulární diagnostická technologie.
Současná technologie detekuje pouze určité oblasti viru, zatímco technologie molekulární diagnostiky umožňuje detekci pouze v případě přítomnosti RNA viru SARS-CoV-2, což umožňuje rychlou identifikaci.
Podle profesora Lee má varianta Omicron silný signál pro geny N v PCR testech, ale slabý signál pro geny S.
U varianty „Stealth Omicron“ byly pozitivní jak geny N, tak S, což ztěžuje její odlišení od ostatních variant.
Molekulární diagnostická technologie funguje na různých principech než PCR a účinně detekuje variantu Omicron.
Oproti konvenční technologii, která zpracovává obvykle až 96 vzorků zařízením, dokáže nová technologie zpracovat za 30 minut více než 125 vzorků (více než 250 vzorků za hodinu).
Kromě toho tato technologie nevyžaduje speciální vybavení, takže je možné snadno a jednoduše vyrábět diagnostické soupravy.
Protože tento postup umožňuje vyvinout diagnostický test za pouhých čtyři dny, lze očekávat, že i v budoucnu bude reagovat rychle, pokud se objeví nová varianta nebo virus.
„Věřím, že zveřejnění této technologie nám umožní co nejdříve vrátit se k normálnímu každodennímu životu,“ řekl profesor Lee.
Budeme se snažit rychle identifikovat a reagovat na nové varianty, které se mohou objevit po skončení pandemie COVID-19.
Tato technologie je nyní ve fázi komercializace.
Nicméně v současných situacích, kdy ještě nebyl vyvinut PCR test na Omicron, může být použit jako pomocný nástroj.
Profesor Lee řekl: „Myslím, že tato technologie bude v druhé polovině příštího roku téměř připravena k komerčnímu využití po ukončení klinických testů.“
