Molekulární diagnostický test dokáže detekovat variantu Omicron za 20 minut
Korejští vědci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicron.
Vývoj technologie byl v současné době dokončen a očekává se, že bude trvat nějaký čas na komercializaci.
POSTECH 10. oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicron za pouhých 20–30 minut, a výsledky zveřejní online.
Omicron je varianta, ve které je 26-32 mutací v hrotu, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na bázi jednoho nukleotidu, takže může detekovat "Stealth Omicron", které je obtížné detekovat testy PCR.
V současné době korejská centra pro kontrolu a prevenci nemocí používají k detekci variant COVID-19 tři metody: analýzu celého genomu, analýzu cílové DNA (mutace, jako je špičatý protein) a test PCR.
V případě varianty Delta ji lze detekovat současným PCR testem, ale Omicron ne.
Nově vyvinutou technologií tentokrát není metoda sekvenování, která čte sekvence DNA nebo RNA, ale technologie molekulární diagnostiky.
Stávající technologie skenuje pouze specifické oblasti viru, ale technologie molekulární diagnostiky byla navržena tak, aby vyvolávala vazebné reakce nukleové kyseliny pouze tehdy, když je přítomna RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má Omicron silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě "Stealth Omicron" se oba geny N a S potvrdily pozitivně, takže je obtížné jej odlišit od ostatních variant.
Molekulární diagnostická technologie pracuje s jinými mechanismy než PCR a efektivně detekuje variantu Omicron.
Na rozdíl od konvenční technologie, která obecně zpracovává až 96 vzorků na zařízení, může nová technologie zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje speciální vybavení, takže lze vytvořit diagnostické soupravy jednoduše a snadno.
Vzhledem k tomu, že metoda může vyvinout diagnostickou sadu za 4 dny, očekává se, že bude reagovat rychle, i když se v budoucnu objeví nová varianta nebo virus.
"Doufám, že zveřejnění této technologie nám pomůže vrátit se do normálního každodenního života co nejdříve," řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které mohou vyjít po COVID-19.
Tato technologie je v současné době před komercializací.
Může však být použit jako pomocný v současných situacích, kdy nebyl vyvinut PCR test na Omikron.
Profesor Lee řekl: "Myslím, že tato technologie bude blízko komercializaci ve druhé polovině příštího roku po klinických studiích.
