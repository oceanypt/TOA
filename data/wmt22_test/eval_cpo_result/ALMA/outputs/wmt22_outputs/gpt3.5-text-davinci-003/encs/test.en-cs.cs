Omicron je varianta, ve které jsou 26-32 mutace ve spike, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišovat mutace na úrovni jednotlivých nukleotidů, takže může detekovat "Stealth Omicron", které jsou obtížně detekovatelné PCR testy.
V současné době Korea Centers for Disease Control and Prevention používá tři metody k detekci variant COVID-19: analýzu celého genomu, analýzu cílové DNA (mutace, jako je například spike protein) a PCR test.
V případě varianty Delta ji lze zjistit pomocí současného PCR testu, ale Omicron ne.
Nově vyvinutá technologie tentokrát není metoda sekvenování, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Technologie, která je k dispozici, zobrazuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby vyvolávala reakce pouze při přítomnosti RNA COVID-19, čímž umožňuje rychlé zjištění.
Podle profesora Lee má Omicron silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě "Stealth Omicron" byly obě N a S geny potvrzeny jako pozitivní, což ztěžuje jeho odlišení od ostatních variant.
Molekulární diagnostická technologie pracuje na odlišných mechanismech od PCR, účinně detekující variantu Omicron.
Na rozdíl od běžné technologie, která obvykle zpracovává až 96 vzorků na zařízení, nová technologie dokáže zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Navíc tato technologie nepotřebuje speciální vybavení, takže může vytvářet diagnostické sady jednoduše a snadno.
Metoda může vyvinout diagnostický kit během 4 dnů, takže se očekává, že bude schopna rychle reagovat i v případě, že se v budoucnu objeví nová varianta nebo virus.
