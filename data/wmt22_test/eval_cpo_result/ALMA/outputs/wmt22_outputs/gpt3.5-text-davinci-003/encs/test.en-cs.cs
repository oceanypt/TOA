Autorka knihy 'Rozhovor s upírem' Anne Rice zemřela ve věku 80 let.
Ona zemřela kvůli komplikacím vyplývajícím z mrtvice, řekl Christopher Rice.
Největším úspěchem Riceové bylo její první román "Rozhovor s upírem", který byl vydán v roce 1976 a představil postavu upíra Lestata, který byl hlavní postavou ve 13 knihové sérii Kronik, z nichž nejnovější byla vydána v roce 2018.
Rice řekl na přednášce na Southern Illinois University v roce 2010: „Měl jsem představu o Lestatovi jako o muži akce, muži, který může dělat věci, které já nemůžu.“
"Interview s upírem" bylo zfilmováno úspěšným filmem v roce 1994, což pomohlo obnovit zájem o žánr upírů, který pokračoval seriálem "The Vampire Diaries" a filmovou sérií "Twilight".
I když žila většinu svého života v Kalifornii, Rice byla rodilou Neworleánčankou a podle její biografie na webu nastavila mnoho svých příběhů právě tam.
Christopher Rice, syn Riceových, řekl, že byl u postele své matky, když zemřela.
Anne Rice bude pohřbena v soukromém obřadu v New Orleansu, s veřejnou památnou slavností plánovanou na příští rok, řekl.
