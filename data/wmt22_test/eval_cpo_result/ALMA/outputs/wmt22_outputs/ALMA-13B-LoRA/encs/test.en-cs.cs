Hrozba byla učiněna v sobotu několik hodin poté, co Británie souhlasila vydat dalších 23 licencí francouzským rybářům, aby zmírnila napětí mezi sousedy, kteří se v posledních šesti měsících dohadovali kvůli rybolovu.
