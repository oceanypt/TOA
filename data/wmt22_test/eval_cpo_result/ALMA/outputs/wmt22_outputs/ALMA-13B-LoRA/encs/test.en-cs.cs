V neděli Jihoafrická republika oficiálně uctila FW de Klerka, posledního prezidenta bílé nadvlády, který osvobodil Nelsona Mandelu z vězení a přivedl zemi z apartheidu k demokracii.
De Klerk zemřel 11. listopadu ve věku 85 let po boji s rakovinou.
Na jeho počest byly vyhlášeny čtyři dny národního smutku.
Působil jako prezident od roku 1989 do roku 1994 a je nejvíce vzpomínán na to, že vedl přechod Jihoafrické republiky od bělošské menšinové vlády k prvním multirasovým volbám v roce 1994.
De Klerk také sdílel Nobelovu cenu míru s Mandelou v roce 1993 po jeho propuštění z vězení v roce 1990.
Mandela se pak stal prvním černým prezidentem Jihoafrické republiky po vítězství jeho Afrického národního kongresu v roce 1994.
Prezident Cyril Ramaphosa v neděli ráno navštívil protestantskou Groote Kerk v Kapském Městě - jednu z nejstarších církevních budov v Jihoafrické republice - aby zde vzdal hold De Klerkovi.
„Byl často nesprávně chápán kvůli své přecitlivělosti,“ řekla Elita Georgiadisová, vdova po De Klerkovi, přibližně 200 účastníkům.
Nikdy nezapomenu na tohoto muže, který mě okouzlil, který mě přesvědčil, že chci mu pomoci dosáhnout jeho obrovského úkolu.
