Navzdory pozitivní pověsti v zahraničí byl De Klerk v Jihoafrické republice kontroverzní postavou a jeho smrt vyvolala smíšené reakce.
Kritici říkají, že zůstává neoddělitelný od zločinů z doby apartheidu a mohl být za ně zodpovědný, kdyby žil déle.
De Klerk zastupoval Národní stranu, která v roce 1948 formálně zavedla rasovou segregaci a diskriminaci většinové nebílé populace Jihoafrické republiky.
Vně kostela malá skupina protestujících držela transparenty s nápisy „Spravedlnost odepřena“ a „Spravedlnost pro oběti apartheidu“ a byla rychle odvedena policií.
Předměstí bylo uzavřeno pro dopravu a bylo zde zvýšené bezpečnostní opatření.
