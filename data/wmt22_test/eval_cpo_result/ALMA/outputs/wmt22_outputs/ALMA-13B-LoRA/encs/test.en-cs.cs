Test molekulární diagnostiky dokáže detekovat variantu Omicron za 20 minut: Zpráva
Korejští vědci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicron.
Vývoj technologie je nyní dokončený a očekává se, že bude trvat čas na komercializaci.
POSTECH oznámil 10. prosince, že výzkumný tým pod vedením profesora Lee Jung-wooka z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicron za 20-30 minut a výsledky zveřejní online.
Omikron je varianta s 26 až 32 mutacemi ve spike, což je protein používaný k infekci buněk virem COVID-19.
Podle výzkumného týmu dokáže technologie molekulární diagnostiky rozpoznat mutace na úrovni jednotlivé nukleotidové báze, takže dokáže detekovat „Stealth Omicron“, které jsou obtížně detekovatelné pomocí PCR testů.
Korejské centrum pro kontrolu a prevenci nemocí aktuálně používá tři metody k detekci variant COVID-19: analýzu celého genomu, analýzu cílové DNA (mutace jako například spike protein) a PCR test.
V případě varianty Delta se dá detekovat současným PCR testem, ale u varianty Omicron ne.
Nově vyvinutá technologie tentokrát není sekvenační metoda, která čte DNA nebo RNA sekvence, ale molekulární diagnostická technologie.
Existující technologie testuje pouze určité oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby reagovala pouze v případě, že existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má varianta Omicron silný signál pro geny N v PCR testech, ale slabý signál pro geny S.
U varianty „Stealth Omicron“ byly oba geny N i S pozitivní, což ztěžuje její odlišení od jiných variant.
Molekulární diagnostická technologie funguje na různých mechanismech než PCR a efektivně detekuje variantu Omicron.
Oproti konvenční technologii, která zpracovává obvykle až 96 vzorků zařízením, dokáže nová technologie zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nevyžaduje speciální vybavení, takže je možné diagnostické soupravy vyrábět jednoduše a snadno.
Pokud se v budoucnu objeví nová varianta nebo virus, očekává se rychlá reakce, protože metoda dokáže vyvinout diagnostický set za 4 dny.
„Doufám, že zveřejnění této technologie nám pomůže co nejdříve vrátit se do normálního každodenního života,“ řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které se mohou objevit po COVID-19.
Tato technologie je nyní před komercializací.
Může se však používat jako pomocná metoda v současných situacích, kdy PCR test na Omicron nebyl vyvinut.
Profesor Lee řekl: „Myslím, že tato technologie bude blízko komercializace v druhé polovině příštího roku po klinických zkouškách.
Důvodem, proč odhaluji technologii, je sdílet ji s ostatními, abychom společně vyvinuli lepší technologie pro překonání COVID-19 a umožnili také rozvojovým zemím analyzovat varianty COVID-19.“
