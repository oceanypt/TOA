Anne Riceová, autorka románu Interview s upírem, zemřela ve věku 80 let
Zemřela na komplikace způsobené mrtvicí, řekl Christopher Rice.
Riciným největším úspěchem byl její první román „Rozhovor s upírem“, který vyšel v roce 1976 a představil postavu upíra Lestata, která se stala hlavní postavou 13dílné série „Kroniky“, poslední díl vyšel v roce 2018.
„Měl jsem představu, že Lestat je člověk činu, člověk, který dokáže dělat věci, které já nedokážu,“ řekl Rice na přednášce na Southern Illinois University v roce 2010.
„Rozhovor s upírem“ se v roce 1994 proměnil v úspěšný celovečerní film, který obnovil zájem o upíří žánr, který pokračoval v televizním seriálu „Upíří deníky“ a filmové sérii „Zatmění“.
Přestože většinu svého života strávila v Kalifornii, Riceová pocházela z New Orleans a mnoho svých příběhů zasadila právě tam, jak uvádí její webová biografie.
Syn Riceové, Christopher Rice, řekl, že byl u její postele, když zemřela.
Anne Riceová bude pohřbena v soukromé obřadní síni v New Orleans a příští rok bude uspořádán veřejný pietní akt, řekl.
