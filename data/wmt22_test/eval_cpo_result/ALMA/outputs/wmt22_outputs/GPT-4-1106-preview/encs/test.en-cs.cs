Molekulární diagnostický test dokáže zjistit variantu omikron za 20 minut: zpráva
Korejští výzkumníci vyvinuli molekulárně diagnostickou technologii, která dokáže detekovat varianty Omikron.
Vývoj technologie byl v současné době dokončen a očekává se, že komercializace zabere určitý čas.
POSTECH oznámil dne 10., že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže zjistit variantu Omikron během pouhých 20-30 minut, a výsledky budou zveřejněny online.
Omicron je varianta, ve které je 26-32 mutací ve špičce, kterou virus COVID-19 používá k infikování buněk.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišit mutace na úrovni jednotlivých nukleotidových bází, takže dokáže detekovat "Stealth Omikron", který je PCR testy obtížně detekovatelný.
Aktuálně Korea Centers for Disease Control and Prevention používá k detekci variant COVID-19 tři metody: analýza celého genomu, analýza cílené DNA (mutace jako je špičkový protein) a PCR test.
V případě varianty Delta ji lze detekovat současným PCR testem, ale Omikron ne.
Nově vyvinutá technologie tentokrát není sekvencovací metodou, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Současná technologie prohledává pouze konkrétní oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby reakce vázání nukleové kyseliny nastaly pouze tehdy, když existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má Omikron silný signál pro N geny v PCR testech, ale slabý signál pro S geny.
V případě "Stealth Omicron" byly potvrzeny pozitivní oba geny N a S, což ztěžuje odlišení od ostatních variant.
Molekulární diagnostická technologie funguje na jiných principech než PCR, efektivně detekuje variantu Omicron.
Na rozdíl od běžné technologie, která obvykle zpracovává až 96 vzorků na přístroj, nová technologie dokáže zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Navíc tato technologie nepotřebuje specializované vybavení, takže může jednoduše a snadno vytvářet diagnostické sady.
Vzhledem k tomu, že metoda umožňuje vyvinout diagnostickou sadu během 4 dnů, očekává se, že bude možné rychle reagovat i v případě vzniku nové varianty nebo viru v budoucnosti.
"Doufám, že zveřejnění této technologie nám pomůže co nejdříve se vrátit k normálnímu každodennímu životu," řekl profesor Lee.
Usilovně se pokusíme rychle diagnostikovat a reagovat na nové varianty, které mohou vzniknout po COVID-19.
Tato technologie je momentálně před komercializací.
Nicméně může být použit jako pomocný prostředek v současných situacích, kde nebyl vyvinut PCR test na Omikron.
Profesor Lee řekl: "Myslím, že tato technologie bude blízko komercializaci ve druhé polovině příštího roku po klinických zkouškách."
Důvod, proč zveřejňuji technologii, je sdílet ji s ostatními za účelem vyvíjení lepších technologií pro překonání COVID-19 a umožnit také méně rozvinutým zemím analyzovat varianty COVID-19.
